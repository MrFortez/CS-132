public class Fraction {

    private int numerator;
    private int denominator;

    public Fraction() {

    }

    public int getNumerator() {
        return numerator;
    }

    public int getDenominator() {
        return denominator;
    }

    public void setNumerator(int value) {
        numerator = value;
    }

    public void setDenominator(int value) {
        denominator = value;
    }
}
